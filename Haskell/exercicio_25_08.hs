terceiro :: [Int] -> Int
terceiro ls = head (tail (tail ls))

menor :: Int -> Int -> Int 
menor x y 
 | x <= y = x 
 | x > y = y

ultimo :: [Char] -> Char
ultimo str = head (reverse str)

inicio :: [Char] -> [Char]
inicio str = reverse (tail (reverse str))

primeiras :: [Char] -> [Char] -> (Char, Char) 
primeiras a b = (head a, head b)